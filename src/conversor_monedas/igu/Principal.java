    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed

        String conversor = (String) cmbConversor.getSelectedItem();
        
        if(conversor.equalsIgnoreCase("Conversor de Monedas")){
            
            OpcionConversion opcConversion = new OpcionConversion();
            opcConversion.setVisible(true);
            opcConversion.setLocationRelativeTo(null);
        
        } else if (conversor.equalsIgnoreCase("Conversor de Temperatura")){
            OpcionTemperatura temp = new OpcionTemperatura();
            temp.setVisible(true);
            temp.setLocationRelativeTo(null);
        }
        

    }//GEN-LAST:event_btnContinuarActionPerformed

